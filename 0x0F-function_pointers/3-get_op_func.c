#include "3-calc.h"

/**
 *op_add - function to add
 * @a: first int
 * @b: second int
 *Return: the sum
*/


int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - function to substract
 *@a: first integer
 *@b: second int
 *Return: sub
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_div - function to divide
 *@a: first int
 *@b: second int
 *Return: return result
*/

int op_div(int a, int b)
{
	return (a / b);
}


/**
 *op_mod - function to return the remainder
 *@a: first int
 *@b: second int
 *Return: return the result
*/

int op_mod(int a, int b)
{
	return (a % b);
}
